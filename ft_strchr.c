#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (!*s)
		return (NULL);
	while (*s)
	{
		if (*s == (char)c)
		{
			return ((char *)s);
		}
		s++;
	}
	if (!(char)c)
		return ((char *)s + 1);
	return (NULL);
}
